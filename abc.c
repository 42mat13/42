#include <unistd.h>

void	putchar(char c)
{
	write(1, &c, 1);
}

void	abc(void)
{
	int i;

	i = 'a';
	while (i <= 'z')
	{
		putchar(i);
		i++;
	}
}

int		main(void)
{
	abc();
	return (0);
}
